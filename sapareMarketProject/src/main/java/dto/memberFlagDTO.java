package dto;

public class memberFlagDTO {

	private int memberFlagNo;
	private String memberName, memberFlaggerName, memberFlagCategory, memberFlagContent, memberFlagRes;
	
	public int getMemberFlagNo() {
		return memberFlagNo;
	}
	public void setMemberFlagNo(int memberFlagNo) {
		this.memberFlagNo = memberFlagNo;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberFlaggerName() {
		return memberFlaggerName;
	}
	public void setMemberFlaggerName(String memberFlaggerName) {
		this.memberFlaggerName = memberFlaggerName;
	}
	public String getMemberFlagCategory() {
		return memberFlagCategory;
	}
	public void setMemberFlagCategory(String memberFlagCategory) {
		this.memberFlagCategory = memberFlagCategory;
	}
	public String getMemberFlagContent() {
		return memberFlagContent;
	}
	public void setMemberFlagContent(String memberFlagContent) {
		this.memberFlagContent = memberFlagContent;
	}
	
	public String getMemberFlagRes() {
		return memberFlagRes;
	}
	public void setMemberFlagRes(String memberFlagRes) {
		this.memberFlagRes = memberFlagRes;
	}
	
} // end class