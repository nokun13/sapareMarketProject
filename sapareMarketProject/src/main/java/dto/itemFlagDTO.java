package dto;

public class itemFlagDTO {

	private String memberName, itemFlaggerName, itemFlagCategory, itemFlagContent, itemFlagRes;
	private int itemFlagNo, itemId;
	
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getItemFlaggerName() {
		return itemFlaggerName;
	}
	public void setItemFlaggerName(String itemFlaggerName) {
		this.itemFlaggerName = itemFlaggerName;
	}
	public String getItemFlagCategory() {
		return itemFlagCategory;
	}
	public void setItemFlagCategory(String itemFlagCategory) {
		this.itemFlagCategory = itemFlagCategory;
	}
	public String getItemFlagContent() {
		return itemFlagContent;
	}
	public void setItemFlagContent(String itemFlagContent) {
		this.itemFlagContent = itemFlagContent;
	}
	public int getItemFlagNo() {
		return itemFlagNo;
	}
	public void setItemFlagNo(int itemFlagNo) {
		this.itemFlagNo = itemFlagNo;
	}
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	
	public String getItemFlagRes() {
		return itemFlagRes;
	}
	public void setItemFlagRes(String itemFlagRes) {
		this.itemFlagRes = itemFlagRes;
	}
	
} // end class